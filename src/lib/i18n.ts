const translations: Record<Locale, Record<string, string | readonly string[]>> = { en, de };

export function t(key: TranslationKey, locale: Locale = 'en'): string {
  const val = translations[locale]?.[key] || translations.en[key] || key;
  return typeof val === 'string' ? val : val[0] ?? key;
}

export function tArray(key: TranslationKey, locale: Locale = 'en'): readonly string[] {
  const val = translations[locale]?.[key] || translations.en[key];
  if (Array.isArray(val)) return val;
  return typeof val === 'string' ? [val] : [key];
}

export function getLocaleFromStorage(): Locale {
  if (typeof window === 'undefined') return 'en';
  return (localStorage.getItem('buildflow-locale') as Locale) || 'en';
}

export function setLocaleToStorage(locale: Locale): void {
  if (typeof window !== 'undefined') {
    localStorage.setItem('buildflow-locale', locale);
  }
}
